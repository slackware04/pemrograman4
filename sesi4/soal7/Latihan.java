public class Latihan
{
	public static void main(String args[])
	{
		int firstNumber, secondNumber, results; //deklarasi variable firstNumber, secondNumber, results
		firstNumber = 20; //inisiasi variable firstNumber dengan nilai 20
		secondNumber = 10; //inisiasi variable secondNumber dengan nilai 10

		results = firstNumber + secondNumber; //inisiasi variable results dengan penjumlahan antara variabel firstNumber dan secondNumber
		System.out.println(results); //menampikan nilai variabel results
	}
}
