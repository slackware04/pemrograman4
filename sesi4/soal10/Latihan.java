public class Latihan
{
	public static void main(String args[])
	{
		int num = 11; //deklarasi dan inisiasi variabel num dengan nilai 11
		if(num > 5) //perulangan dengan IF dengan syarat variabel num > 5
		{
			System.out.println("Number is Greater than 5"); //menampilkan "Number is Greater than 5"
		}
		else //pengalihan jika syarat tidak terpenuhi
		{
			System.out.println("Number is Less than 5"); //menampilkan "Number is Less than 5"
		}
	}
}
