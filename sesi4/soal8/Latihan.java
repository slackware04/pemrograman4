public class Latihan
{
	public static void main(String args[])
	{
		int count = 1; //deklarasi dan inisisasi variabel count dengan nilai 1
		while (count < 5) //perulangan dengan while dengan syarat variabel count < 5
		{
			System.out.println("Count is equal to " + count); //menampilkan string : "Count is equal to" dan nilai dari variabel count
			count ++; //menambahkan variabel count dengan nilai 1 digit
		}
	}
}
